const std = @import("std");
const common = @import("common");
const zigxCompiler = @import("zigxCompiler");

const Writer = common.Writer;

pub fn generate(allocator: std.mem.Allocator, output_path: []const u8, documents: []const zigxCompiler.ZigxDocument) !void {
    var out_file = try std.fs.cwd().createFile(output_path, .{});
    defer out_file.close();

    var buf: [8192]u8 = undefined;
    var writer = out_file.writer(&buf);
    const w = &writer.interface;

    try writeHeader(w);
    try writeImports(w, documents);
    try writeRegisterFunction(w, documents);
    try writer.interface.flush();

    _ = allocator;
}

fn writeHeader(w: *Writer) !void {
    try w.writeAll(
        \\// Auto-generated file - DO NOT EDIT
        \\// Generated by build/gen_zigx.zig
        \\
        \\const app_mod = @import("../Framework/Http/Server/app.zig");
        \\const App = app_mod.App;
        \\
        \\// Import server handlers
        \\
    );
}

fn writeImports(w: *Writer, documents: []const zigxCompiler.ZigxDocument) !void {
    for (documents) |doc| {
        try w.print(
            \\const {s}_server = @import("server/{s}.zig");
            \\
        , .{ doc.file_name, doc.file_name });
    }
}

fn writeRegisterFunction(w: *Writer, documents: []const zigxCompiler.ZigxDocument) !void {
    try w.writeAll(
        \\
        \\pub fn registerRoutes(app: *App) void {
        \\
    );

    for (documents) |doc| {
        try w.print(
            \\    app.get("{s}", {s}_server.handler);
            \\
        , .{ doc.route, doc.file_name });
    }

    try w.writeAll(
        \\}
        \\
    );
}
